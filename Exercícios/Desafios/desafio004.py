n = input('Digite algo: ')
print('É alphanumerico ?', n.isalnum())
print('É alfabético?', n.isalpha())
print('É numerico ?', n.isnumeric())
print('Está capitalizado?', n.istitle()) #n.istitle é para verificar se a primeira letra é maiuscula
print('Esta em maiúsculo ? ', n.isupper())
print('Esta em minusculo? ', n.islower())
